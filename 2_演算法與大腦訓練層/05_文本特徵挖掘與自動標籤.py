#!/usr/bin/env python
# -*- coding: utf-8 -*-
"""
專案名稱：營造業職災風險自動化評估系統
模組名稱：1_數據工程與法律合規層 / 05_文本特徵挖掘與自動標籤.py
功能描述：文字探勘工程（Text Mining）。從多源融合母表的純文字敘述中，
          利用精準正規表達式與營造業專用辭典，自動挖掘出「勞工年齡」、「工種別」
          以及「職安管理缺失標籤」，並精準匯出至第二層供 AI 建模。
"""

import os
import sys
import re
import pandas as pd
import numpy as np

# 確保支援繁體中文路徑與輸出
sys.stdout.reconfigure(encoding='utf-8') if hasattr(sys.stdout, 'reconfigure') else None

def 智慧提取年齡(文本):
    """
    從職災敘事文本中精準提取勞工年齡。
    防禦點：排除可能匹配到年份（如110年）、高度（如5公尺）或金額的數字。
    """
    if not isinstance(文本, str) or pd.isna(文本):
        return np.nan
        
    # 模式1：匹配「某某某(男，45歲)」或「(女，32歲)」
    模式1 = re.search(r"[\(（][男女]\s*[，,]\s*(\d{2})\s*歲[\)）]", 文本)
    if 模式1:
        return int(模式1.group(1))
        
    # 模式2：匹配「約45歲」、「現年32歲」、「一名25歲之勞工」
    模式2 = re.search(r"(?:約|現年|名|位|\s)(\d{2})\s*歲(?:\s|之|勞|民|外)", 文本)
    if 模式2:
        return int(模式2.group(1))
        
    return np.nan

def 智慧識別工種(文本, 工種辭典):
    """
    掃描文本，根據營造業專家辭典自動識別並分類工種。
    若無匹配則歸類為 '一般雜工/其他'。
    """
    if not isinstance(文本, str) or pd.isna(文本):
        return "未知"
        
    for 標準工種, 關鍵字清單 in 工種辭典.items():
        for 關鍵字 in 關鍵字清單:
            if 關鍵字 in 文本:
                return 標準工種
                
    return "一般雜工/其他"

def main():
    # =========================================================================
    # 1. 跨層級智慧型路徑設定與資料載入
    # =========================================================================
    第一層路徑 = "D:/營造業職災風險自動化評估系統/1_數據工程與法律合規層"
    第二層路徑 = "D:/營造業職災風險自動化評估系統/2_演算法與大腦訓練層"
    
    輸入路徑 = os.path.join(第一層路徑, "多源異質大數據融合母表.csv")
    輸出路徑 = os.path.join(第二層路徑, "05_特徵挖掘與自動標籤母表.csv")
    
    print("==================================================")
    print("🚀 正在啟動 [文本特徵挖掘與自動標籤系統] ...")
    print("==================================================")
    
    # 防禦機制：自動確保第二層的實體資料夾存在 [5]
    if not os.path.exists(第二層路徑):
        os.makedirs(第二層路徑, exist_ok=True)
        print(f"💡 [系統提示] 自動建立第二層資料夾：{第二層路徑}")
    
    if not os.path.exists(輸入路徑):
        print(f"❌ 找不到上游融合母表，請先執行 04 號腳本產出檔案：{輸入路徑}")
        return
        
    try:
        df = pd.read_csv(輸入路徑, encoding="utf-8-sig")
        print(f"📊 成功載入第一層融合母表，共 {len(df)} 筆原始職災個案。")
    except Exception as e:
        print(f"❌ 讀取融合母表失敗，原因: {e}")
        return

    # =========================================================================
    # 2. 定義營造業領域專家知識庫 (Domain Knowledge Base)
    # =========================================================================
    # A. 營造業核心工種分類辭典
    營造工種辭典 = {
        "模板工": ["模板", "板模", "撐柱", "拆模"],
        "鋼筋工": ["鋼筋", "綁紮", "壓接", "剪斷機"],
        "鷹架工": ["鷹架", "施工架", "搭設", "拆除施工架", "足場"],
        "泥水工": ["泥水", "水泥", "粉刷", "貼磚", "灌漿", "混凝土"],
        "水電工": ["水電", "配線", "配管", "接線", "電工", "變壓器"],
        "鐵工/鋼構工": ["鐵工", "鋼構", "電焊", "乙炔", "切割", "起重吊掛"],
        "重機操作工": ["吊車", "怪手", "挖土機", "起重機", "推土機", "山貓"],
        "油漆防水工": ["油漆", "漆工", "防水", "塗料", "有機溶劑"],
        "解體/清潔工": ["解體", "拆除工", "清潔", "打石", "清運"]
    }
    
    # B. 職安管理缺失關鍵字矩陣 (用於 One-Hot 標籤挖掘)
    缺失定義 = {
        "缺失_未配戴安全帶": ["未配戴安全帶", "未繫安全帶", "未戴安全帶", "未使用安全帶"],
        "缺失_未配戴安全帽": ["未配戴安全帽", "未戴安全帽", "未使用安全帽"],
        "缺失_未設置護欄": ["未設置護欄", "無護欄", "護欄破壞", "未設護欄"],
        "缺失_未設置防墜網": ["未設置防墜網", "無安全網", "防墜網", "安全網缺失"],
        "缺失_未設置擋土支撐": ["擋土支撐", "開挖面崩塌", "未設支撐", "崩塌防護"],
        "缺失_未斷電作業": ["未斷電", "活線作業", "檢電", "未停電"],
        "缺失_無專人指揮監督": ["未指派專人", "無指揮", "缺少監督", "無人在場指揮"]
    }

    # =========================================================================
    # 3. 執行智慧文字探勘工程 (Text Mining Engineering)
    # =========================================================================
    print("⏳ 正在深度挖掘文本特徵並自動打上高維度標籤...")
    
    # 智慧鎖定文本欄位
    文本欄位 = next((col for col in ["災害原因簡述", "事故經過", "摘要描述", "災害類型"] if col in df.columns), None)
    
    if not 文本欄位:
        df["綜合文本"] = df.astype(str).agg(' '.join, axis=1)
        文本欄位 = "綜合文本"
    
    # 特徵 1：提取年齡與中位數補值防禦
    df["特徵_勞工年齡"] = df[文本欄位].apply(智慧提取年齡)
    年齡中位數 = df["特徵_勞工年齡"].median()
    if pd.isna(年齡中位數) or 年齡中位數 == 0:
        年齡中位數 = 45.0
    df["特徵_勞工年齡"] = df["特徵_勞工年齡"].fillna(年齡中位數).astype(int)
    
    # 特徵 2：自動分類工種別
    df["特徵_工種別"] = df[文本欄位].apply(lambda x: 智慧識別工種(x, 營造工種辭典))
    
    # 特徵 3：自動生成多源職安管理缺失 One-Hot 標籤
    for 標籤欄位, 關鍵字清單 in 缺失定義.items():
        df[標籤欄位] = 0
        for 關鍵字 in 關鍵字清單:
            df.loc[df[文本欄位].str.contains(關鍵字, na=False), 標籤欄位] = 1

    # =========================================================================
    # 4. 結構化橫向跨層級匯出「AI 訓練特徵矩陣」
    # =========================================================================
    try:
        df.to_csv(輸出路徑, index=False, encoding="utf-8-sig")
        print("\n==================================================")
        print(f"🎉 [成功] 05 號文本特徵挖掘與自動標籤工程完成！")
        print(f"📊 標籤化規模：{df.shape} (筆數, 擴充特徵總數)")
        print(f"🕵️‍♂️ 成功挖掘特徵：'特徵_勞工年齡'、'特徵_工種別'")
        print(f"🏷️  已自動注入 7 大類職安管理缺失風險標籤")
        print(f"📂 儲存路徑：{輸出路徑}")
        print("==================================================")
    except Exception as e:
        print(f"❌ 匯出特徵矩陣失敗，原因: {e}")

if __name__ == "__main__":
    main()
