"""
專案名稱：營造業職災風險自動化評估系統
模組名稱：1_數據工程與法律合規層 / 04_時空模糊匹配融合.py
功能描述：自動合併 01-13 職災 Excel 分頁，並融合氣象科學與歷史法令裁罰數據，透過快取模糊匹配技術產出合規黃金母體。
"""

import os
import sys
import re
import glob
import pandas as pd
import numpy as np
from fuzzywuzzy import process

# 確保支援繁體中文路徑與輸出
sys.stdout.reconfigure(encoding='utf-8') if hasattr(sys.stdout, 'reconfigure') else None

def 智慧合併職災分頁檔案(基礎路徑):
    """
    智慧型分頁合併器：
    自動搜尋資料夾內所有 '營造業105_115年歷史職災數據庫_網頁分頁*.xlsx' 檔案，
    並將其全部合併為一個完整的 df_職災 數據集。
    """
    搜尋樣式 = os.path.join(基礎路徑, "營造業105_115年歷史職災數據庫_網頁分頁*.xlsx")
    分頁檔案清單 = glob.glob(搜尋樣式)
    
    if not 分頁檔案清單:
        # 降級防禦：嘗試尋找不含網頁分頁字眼的主表
        主表路徑 = os.path.join(基礎路徑, "營造業105_115年歷史職災數據庫.csv")
        if os.path.exists(主表路徑):
            print(f"💡 [提示] 未發現分頁檔，改為載入職災單一主表...")
            return pd.read_csv(主表路徑, encoding='utf-8-sig', on_bad_lines='skip')
        raise FileNotFoundError(f"❌ 找不到任何職災數據分頁檔案！請檢查路徑：{搜尋樣式}")
    
    # 排序檔案確保從 分頁01 吃到 分頁13
    分頁檔案清單.sort()
    
    全部資料表 = []
    print(f"📦 偵測到 {len(分頁檔案清單)} 個職災數據分頁檔案，開始全自動讀取與合併...")
    
    for 檔案路徑 in 分頁檔案清單:
        檔名 = os.path.basename(檔案路徑)
        try:
            df_分頁 = pd.read_excel(檔案路徑)
            if not df_分頁.empty:
                全部資料表.append(df_分頁)
                print(f"   [成功] 讀入分頁：{檔名} (共 {len(df_分頁)} 筆)")
        except Exception as e:
            print(f"   [警告] 讀取分頁 {檔名} 失敗，原因: {e}")
            
    if not 全部資料表:
        raise ValueError("❌ 讀入的所有分頁資料皆為空值，無法合併！")
        
    合併後的df = pd.concat(全部資料表, ignore_index=True)
    print(f"✅ 所有分頁合併完成！職災母體總筆數：{len(合併後的df)} 筆。\n")
    return 合併後的df

def main():
    # =========================================================================
    # 1. 基礎路徑與實體資料安全載入
    # =========================================================================
    基礎路徑 = "D:/營造業職災風險自動化評估系統/1_數據工程與法律合規層"
    
    print("🚀 正在啟動 [多分頁整合一體型] 時空模糊匹配融合系統...")
    
    try:
        # A. 自動合併 1~13 的職災分頁 Excel 檔案
        df_職災 = 智慧合併職災分頁檔案(基礎路徑)
        
        # B. 載入氣象因子庫 CSV 檔案
        氣象路徑 = os.path.join(基礎路徑, "職災個案當日氣象環境因子庫.csv")
        df_氣象 = pd.read_csv(氣象路徑, encoding="utf-8-sig", engine="python", on_bad_lines="skip")
        print(f"📊 氣象數據載入成功，共 {df_氣象.shape[0]} 筆。")
        
        # C. 載入歷史法令裁罰母表 CSV 檔案
        裁罰路徑 = os.path.join(基礎路徑, "歷史法令裁罰原始母表.csv")
        df_裁罰 = pd.read_csv(裁罰路徑, encoding="utf-8-sig", engine="python", on_bad_lines="skip")
        print(f"📊 裁罰紀錄載入成功，共 {df_裁罰.shape[0]} 筆。")
        
    except Exception as e:
        print(f"\n❌ 核心數據載入階段發生嚴重錯誤：{e}")
        return

    # =========================================================================
    # 2. 裁罰公司欄位動態偵測與模糊匹配快取建立
    # =========================================================================
    公司欄位 = None
    for col in ["公司名稱", "處分對象", "被處分人", "營造廠"]:
        if col in df_裁罰.columns:
            公司欄位 = col
            break
    if not 公司欄位:
        公司欄位 = df_裁罰.columns[0] # 找不到就預設取第一欄
        
    裁罰公司清單 = df_裁罰[公司欄位].dropna().unique().tolist()
    
    # 初始化高效快取字典與資料拼接清單
    fuzz_cache = {}
    融合大數據清單 = []

    # =========================================================================
    # 3. 核心大數據融合迴圈 (點對點時空與法律對齊)
    # =========================================================================
    print("\n⏳ 正在對合併後的母體進行時空對齊與法律合規比對，請稍候...")
    
    for index, 職災個案 in df_職災.iterrows():
        # --- [A] 基礎特徵提取 ---
        職災日期 = str(職災個案.get("發生日期時間", "")).strip()
        職災地址 = str(職災個案.get("事故詳細地址", "")).strip()
        原始公司 = str(職災個案.get("公司名稱", "")).strip()
        
        # 地理空間特徵提取 (縣市)
        縣市搜尋 = re.search(r"(\w{3}(縣|市))", 職災地址)
        職災縣市 = 縣市搜尋.group(1) if 縣市搜尋 else "未明"

        # --- [B] 法律合規：FuzzyWuzzy 模糊匹配與歷史裁罰加總 ---
        歷史裁罰總額 = 0.0
        匹配公司名稱 = "無匹配"
        
        if 原始公司 and 原始公司 != "nan" and 原始公司 != "":
            if 原始公司 in fuzz_cache:
                匹配結果 = fuzz_cache[原始公司]
            else:
                匹配結果 = process.extractOne(原始公司, 裁罰公司清單)
                fuzz_cache[原始公司] = 匹配結果
            
            # 確保匹配結果存在，且相似度分數 (index 1) >= 80
            if 匹配結果 and 匹配結果[1] >= 80:
                匹配公司名稱 = 匹配結果[0]
                
                # 加總該公司在裁罰表中的所有罰鍰金額，並自動排除異常文字
                歷史裁罰金額篩選 = df_裁罰[df_裁罰[公司欄位] == 匹配公司名稱]['罰鍰金額']
                歷史裁罰總額 = pd.to_numeric(歷史裁罰金額篩選, errors='coerce').sum()

        # --- [C] 科學氣象：時空對齊布林遮罩與安全取值 ---
        日期條件 = df_氣象['發生日期時間(對照組)'] == 職災日期
        縣市條件 = df_氣象['對齊縣市'].str.contains(職災縣市, na=False)
        配對氣象 = df_氣象[日期條件 & 縣市條件]

        if not 配對氣象.empty:
            # 修正點：使用最安全的 pandas .iloc[0] 字典型取值，防範欄位缺失或破格
            降雨 = 配對氣象.iloc[0].get("當日降雨量(mm)", 0.0)
            風速 = 配對氣象.iloc[0].get("最大瞬間風速(m/s)", 2.5)
            高溫 = 配對氣象.iloc[0].get("當日最高氣溫(°C)", 28.0)
        else:
            降雨, 風速, 高溫 = 0.0, 2.5, 28.0

        # --- [D] 多源異質欄位橫向拼接 ---
        融合行 = {
            "發生日期時間": 職災日期,
            "對齊縣市": 職災縣市,
            "原始公司名稱": 原始公司,
            "Fuzzy匹配公司": 匹配公司名稱,
            "災害類型": 職災個案.get("災害類型", "無資料"),
            "罹災人數": 職災個案.get("罹災人數", 0),
            "特徵_歷史裁罰總額": 歷史裁罰總額,
            "特徵_當日降雨量(mm)": 降雨,
            "特徵_最大瞬間風速(m/s)": 風速,
            "特徵_當日最高氣溫(°C)": 高溫
        }
        融合大數據清單.append(融合行)

    # =========================================================================
    # 4. 結構化匯出「黃金母體」大數據融合總表
    # =========================================================================
    if 融合大數據清單:
        融合表格 = pd.DataFrame(融合大數據清單)
        輸出路徑 = os.path.join(基礎路徑, "多源異質大數據融合母表.csv")
        融合表格.to_csv(輸出路徑, index=False, encoding="utf-8-sig")
        
        print("\n==================================================")
        print(f"🎉 [成功] 13個分頁自動合併且黃金母體融合完成！")
        print(f"📊 最終整合規模：{融合表格.shape} (總筆數, 融合特徵數)")
        print(f"📂 儲存路徑：{輸出路徑}")
        print("==================================================")
    else:
        print("⚠️ 警告：融合清單為空，請檢查原始資料結構是否正確。")

if __name__ == "__main__":
    main()
